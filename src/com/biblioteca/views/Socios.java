    private void buscarTxtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buscarTxtMouseClicked
        if(nombreBarra.getText().isEmpty()){
            ArrayList<Socio> socios = new ArrayList<Socio>();
            socios = biblioteca.sociosTabla();
            int i=0;
            for(Socio socio : socios){
                sociosTabla.setValueAt(socio.getNombreSocio(), i, 0);
                sociosTabla.setValueAt(socio.getDireccion(),i,1);
                sociosTabla.setValueAt(socio.getNumPrestados(), i, 2);
                sociosTabla.setValueAt(socio.getNumeroSocio(), i, 3);
                i++;
            } 
        }else{
            try{
                Socio socio = biblioteca.nombreBuscado(nombreBarra.getText());
                sociosTabla.setValueAt(socio.getNombreSocio(), 0, 0);
                sociosTabla.setValueAt(socio.getDireccion(),0,1);
                sociosTabla.setValueAt(socio.getNumPrestados(), 0, 2);
                sociosTabla.setValueAt(socio.getNumeroSocio(), 0, 3);
            }catch (ESocioNoEncontrado e){
                javax.swing.JOptionPane.showMessageDialog(this,"No se tiene registro del socio","ERROR", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_buscarTxtMouseClicked

